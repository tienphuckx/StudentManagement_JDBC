package model;

public class RangesScores {
	private double begin;
	private double end;
	public double getBegin() {
		return begin;
	}
	public void setBegin(double begin) {
		this.begin = begin;
	}
	public double getEnd() {
		return end;
	}
	public void setEnd(double end) {
		this.end = end;
	}
	public RangesScores(double begin, double end) {
		super();
		this.begin = begin;
		this.end = end;
	}
	
	
}
